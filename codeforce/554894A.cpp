void solve()
{
   int n, q;
   cin >> n >> q;
   vector<int> a(n);
   for (auto &c : a) cin >> c;
   sort(all(a), greater<int>());
   vector<int> pref(n + 1);
   for (int i = 1; i <= n; ++i) pref[i] = pref[i - 1] + a[i - 1];
   while (q--) {
      int x;
      cin >> x;
      int p = lower_bound(all(pref), x) - pref.begin();
      cout << (p == sz(pref) ? -1 : p) << '\n';
   }
}


