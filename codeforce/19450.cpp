void solve()
{
    int a, b, c;
    cin >> a >> b >> c;
    if (b % 3 == 0) {
        if (c > 0) {
            cout << a + b / 3 + c / 3 + (c % 3 != 0) << "\n";
            return;
        }
        cout << a + b / 3 << "\n";
    } else {
        int tmp = 3 - b % 3;
        if (tmp > c) {
            cout << -1 << "\n";
            return;
        }
        c -= tmp;
        if (c > 0) {
            cout << a + b / 3 + 1 + c / 3 + (c % 3 != 0) << "\n";
            return;
        }
        cout << a + b / 3 + 1 << "\n";
    }
}


