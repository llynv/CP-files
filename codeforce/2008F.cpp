const int MOD = 1e9 + 7;

int fpow(int a, int b) {
    int res = 1;
    while (b) {
        if (b & 1) res = res * a % MOD;
        a = a * a % MOD;
        b >>= 1;
    }
    return res;
}

void solve()
{
    int n;
    cin >> n;
    vector<int> a(n + 1), sum(n + 1);
    for (int i = 1; i <= n; i++)
    {
        cin >> a[i];
        sum[i] = sum[i - 1] + a[i];
    }
    int tu = 0;
    for (int i = 1; i <= n; i++)
    {
        tu += a[i] * (sum[n] - sum[i]);
    }
    // cout << tu << " ";
    // cout << ((tu * 2) / (n * (n - 1))) % MOD << "\n";
    cout << tu % MOD * fpow(n * (n - 1) / 2, MOD - 2) % MOD << "\n";
}


