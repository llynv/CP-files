const int N = 1e6 + 7;
int fw[N];

void add (int i, int v) {
    for (; i < N; i += i & -i) {
        fw[i] += v;
    }
}

int get (int i) {
    int sum = 0;
    for (; i > 0; i -= i & -i) {
        sum += fw[i];
    }
    return sum;
}

void solve()
{
    int n;
    cin >> n;
    vector<int> a(n);
    for (auto &c : a) cin >> c;
    vector<int> dp(n+1);
    for (int i = 1; i <= n; ++i) {
        int x = get(a[i-1]);
        dp[i] += x;
        add(a[i-1], dp[i]);
    }
    cout << accumulate(all(dp), 0ll) << '\n';
}


